#LOGICAL OPERATORS
#logical and
print(True and True)
print(True and False)
print(False and True)
print(False and False)
print(False and 2)
print(2 and 3)
print(5 and -5)

#logical or
print(True or True)
print(True or False)
print(False or True)
print(False or False)
print(False or 2)
print(2 or 3)
print(5 or -5)

#not
print(not True)
print(not False)
print(False and (not True))
print(False or (not False))
print(not 2)
print(not 3)
print(not -5)


#BITWISE OPERATORS
""" & , | , >> , << , ~ , ^ """
#bitwise & (and)
print(True & 2 )
print( False & 3)
print(1&2)
print(2&2)

#bitwise | (or)
print(True | 2 )
print( False | 3)
print(1|2)
print(3|2)

#bitwise ^ (xor)
print(10 ^ 11)
print(1^-1)
print(3^6)  

#left shift
print(7<<1) #multiply by two for every count no of shifts here 1 is count
print(480<<2) #multuplied by 2 for two times where count is 2

#right shift
print(9>>1) #divide  by two for every count no of shifts here 1 is count
print(462>>2)#divide by 2 for two times where count is 2

